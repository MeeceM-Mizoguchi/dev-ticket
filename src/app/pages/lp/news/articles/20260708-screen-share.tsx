import { ScreenFigure, ShareEntryScreen, ScreenShareScreen } from './screens';

/**
 * 記事本文。プレーンな HTML を書くだけで共通タイポグラフィが適用されます。
 * 画面イメージは実機能（CallWidget の共有ボタン / ScreenShareStage）をトレースした
 * 各モックを使用。「共有ボタンを押す → 共有される → 印を付ける」の流れで説明します。
 */
export default function ScreenShare() {
  return (
    <>
      <p>
        オンライン音声通話中に、自分の画面をメンバーへ共有できる「画面共有機能」を追加実装しました。
        資料や作業中の画面を見せながら話せるので、認識のズレなくスムーズに議論を進められます。
      </p>

      <h2>1. 通話中のウィンドウで「画面共有ボタン」を押す</h2>
      <p>
        画面共有は<strong>音声通話中に利用できる機能</strong>です。
        まずは通話を始めてください（発信方法は
        「オンライン音声通話機能を実装しました」の記事をご覧ください）。
        通話中に画面右下に表示される通話ウィンドウの中に、
        <strong>モニターのマークの「画面共有ボタン」</strong>があります。ここを押すと共有が始まります。
      </p>

      <ScreenFigure label="通話ウィンドウ" caption="ミュートボタンのとなり、モニターのマークが「画面共有ボタン」です">
        <ShareEntryScreen />
      </ScreenFigure>

      <h2>2. 自分の画面が相手に共有される</h2>
      <p>
        共有を開始すると、共有する画面（画面全体・ウィンドウ・タブ）を選ぶ確認が表示され、
        選んだ画面がそのまま相手に映ります。共有中は通話ウィンドウにも
        「あなたが画面共有中」と表示されます。
      </p>

      <h2>3. ポインターや手書きで「ここ」と伝える</h2>
      <p>
        共有画面の上には、直接印を付けられます。
        <strong>共有している人</strong>は赤いポインターで「ここ」と指し示せます。
        <strong>見ている人</strong>は画面に手書きやテキストで書き込めます
        （描いた線・文字は5秒で自動的に消えるので、画面を汚しません）。
      </p>

      <ScreenFigure label="画面共有ステージ" caption="共有画面に、ポインター（共有者）や手書き・テキスト（視聴者）で直接印を付けられます">
        <ScreenShareScreen />
      </ScreenFigure>

      <h2>主な特長</h2>
      <ul>
        <li>通話中にワンクリックで自分の画面を共有</li>
        <li>共有者は赤いポインターで「ここ」と指し示しながら説明できる</li>
        <li>視聴者は共有画面に手書き・テキストで書き込み可能（描いた線・文字は5秒で自動的に消えます）</li>
        <li>共有ウィンドウはドラッグ移動・リサイズ・全画面表示に対応</li>
        <li>対応ブラウザ（Chrome など）では別ウィンドウに切り出し、外部モニターへ移動して表示できる</li>
      </ul>

      <h2>ご注意</h2>
      <p>
        iPad・iPhone や一部のブラウザは、画面共有の<strong>開始</strong>に対応していません
        （共有された画面の<strong>閲覧</strong>は可能です）。
        画面共有はパソコン（Chrome など）からのご利用を推奨します。
      </p>
    </>
  );
}
